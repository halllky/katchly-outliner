        /// <summary>
        /// 新しいChangeLogを作成する情報を受け取って登録する Web API
        /// </summary>
        [HttpPost("create")]
        public virtual IActionResult Create([FromBody] ChangeLogCreateCommand param) {
            if (_applicationService.CreateChangeLog(param, out var created, out var errors)) {
                return this.JsonContent(created);
            } else {
                return BadRequest(this.JsonContent(errors));
            }
        }
